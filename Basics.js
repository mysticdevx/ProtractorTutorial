// print smt in console
console.log("hello world")

// data types - JS is a dynamic data type language. just define with var keyword
var a = 4;
var b = "hello";
console.log(a + " " + b);

// conditional statement
var c = "one";
if (c == "one") {
	console.log("one");
} else if (c == "two") {
	console.log("two");
} else {
	console.log("other");
}

// for loop
for (var i = 1; i < 10; i++) {
	console.log(i);
}

// while
var j = 1;
while (j < 5) {
	console.log("j is " + j);
	j++;
}

// do while
var k = 10;
do {
	console.log(k);
	k++;
} while (k < 10)
	
	